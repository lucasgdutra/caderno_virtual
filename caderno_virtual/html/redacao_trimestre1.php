                <p>São Paulo - Aperfeiçoar a redação não deve ser uma 
                    preocupação exclusiva de vestibulandos, concurseiros ou 
                    profissionais de comunicação.
                </p>

                <p>Afinal, pessoas capazes de escrever de forma clara e objetiva
                    , em bom português, são procuradas e valorizadas em qualquer
                    área de atuação - até a mais técnica.
                </p>

                <p>A qualidade da comunicação no trabalho é diretamente proporcional à 
                    produtividade, afirma Rosângela Cremaschi, professora de comunicação escrita
                    da FAAP e consultora de comunicação empresarial da RC7.
                </p>

                <p>Textos confusos, prolixos ou repetitivos acarretam perda de tempo - o que, no
                    limite, significa prejuízo financeiro para o seu empregador.
                </p>
                <p>Um efeito mais subjetivo, mas não por isso menos grave, diz
                    respeito à sua reputação profissional.
                </p>

                <p>Você já pensou no número de pessoas com quem você entra em
                    contato apenas por escrito no trabalho? Se a sua redação é 
                    ruim, a sua imagem perante os outros provavelmente não é 
                    muito diferente disso.
                </p>

                <p>“E-mails mal escritos, por exemplo, podem prejudicar muito a 
                    forma como um profissional é visto por seus pares, e até 
                    como a sua empresa é percebida pelo mercado”, diz Fabiano
                    Ormaneze, consultor de redação do CPDEC (Centro de Pesquisa,
                    Desenvolvimento e Educação Continuada).
                </p>
                <p>Não quer passar vergonha? Veja a seguir 7 problemas muito
                    frequentes em textos empresariais - e como extingui-los da
                    sua comunicação daqui para a frente:
                </p>
                <section>
                    <p>1. Erros de concordância</p>
                    <p>Você já recebeu um e-mail que começava com a frase “Segue
                        anexo os modelos…”? Segundo a professora Rosângela, 
                        muitos profissionais se atrapalham com a concordância 
                        verbal (o correto seria “seguem”, no exemplo dado) e 
                        nominal (o certo é “anexos”). Outro erro frequente é 
                        flexionar no plural os verbos “haver” (quando o sentido 
                        é o mesmo de existir) e “fazer” (no sentido temporal), 
                        como em “Fazem dois dias” e “Houveram processos”. A 
                        norma culta indica o uso de “faz” e “houve”, 
                        respectivamente.
                    </p>
                </section>
                <section>
                    <p>2. Pontuação incorreta</p> 
                    <p>Vírgulas e pontos finais não são enfeites: eles são 
                        ferramentas básicas para construir o sentido de um texto
                        . Apesar disso, o mau uso desses sinais é um problema 
                        crônico em textos corporativos. “Um erro grave e comum é
                        colocar vírgula entre sujeito e predicado, ou entre o 
                        verbo e seus complementos”, diz Rosângela.
                    </p>
                </section>
                <section>
                    <p>3. Prolixidade</p> 
                    <p>Se você não planeja o que vai escrever, há um risco 
                        grande de se atrapalhar e dar muitas explicações sobre 
                        detalhes irrelevantes. A dica do professor Fabiano é 
                        parar para refletir sobre o objetivo da sua mensagem 
                        antes de começar a digitar. Além disso, prefira frases 
                        curtas para economizar o tempo alheio e reduzir o 
                        potencial de mal-entendidos.
                    </p>
                </section>
                <section>
                    <p>4. Termos obscuros</p> 
                    <p>Siglas, jargões, nomes técnicos, expressões em inglês - 
                        todos esses elementos precisam ser eliminados do seu 
                        texto se o seu receptor não tiver familiaridade com eles
                        . A recomendação é especialmente importante para a 
                        comunicação entre áreas ou departamentos diferentes, diz
                        Fabiano, já que que os interlocutores podem não ter o 
                        mesmo repertório.
                    </p>
                </section>
                <section>
                    <p>5. Uso inadequado do vocabulário</p>
                    <p>Para evitar gafes e equívocos, é obrigatório ter certeza 
                        do significado ou da grafia de uma palavra antes de 
                        incluí-la na sua redação. De acordo com Rosângela, há 
                        muita confusão entre as expressões “estar a par” e “
                        estar ao par”, por exemplo. Enquanto a primeira 
                        significa “estar ciente”, a segunda indica equivalência 
                        entre duas moedas.
                    </p>
                </section>
                <section>
                    <p>6. Falta de sequência lógica</p>
                    <p>Para a professora Rosângela, um texto só é coerente quando
                        conta com começo, meio e fim - sem contradições ou 
                        mudanças bruscas do pensamento. A lógica da sua 
                        mensagem também depende de palavras ou expressões que
                        estabeleçam a transição de ideias, tais como pronomes
                        , advérbios e conjunções. 
                    </p>
                </section>
                <section>
                    <p>7. Redundâncias</p>
                    <p>Por mais que você deseje enfatizar uma informação, é bom 
                        tomar cuidado com repetições e pleonasmos. Além de 
                        desperdiçar o tempo do leitor, esse tipo de deficiência 
                        pode depor contra a sua imagem profissional. “Erros como
                        esse, assim como falta de acentos ou erros de digitação,
                        não comprometem tanto a mensagem, mas prejudicam a sua 
                        reputação e também a do seu empregador”, afirma Fabiano.
                    </p>     
